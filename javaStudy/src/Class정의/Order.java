package Class정의;

public class Order {
	
	String orderNo;
	String orderId;
	String orderDate;
	String orderName;
	String productNo;
	String address;
	
	public String getOrderNo() {
		return orderNo;
	}
	public String getOrderId() {
		return orderId;
	}
	public String getOrderDate() {
		return orderDate;
	}
	public String getOrderName() {
		return orderName;
	}
	public String getProductNo() {
		return productNo;
	}
	public String getAddress() {
		return address;
	}
	
	
	
}
